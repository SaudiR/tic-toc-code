class Challenge:

    def __init__(
        self,
        title,
        topic,
        difficulty,
        description,
        hint,
        points,
        time_limit,
        leetcode_number,
        options,
        answer
    ):

        self.title = title
        self.topic = topic
        self.difficulty = difficulty
        self.description = description
        self.hint = hint
        self.points = points
        self.time_limit = time_limit
        self.leetcode_number = leetcode_number
        self.options = options
        self.answer = answer

